using System;

namespace OperadoresAtribuicao
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int x = 0;
            x += 5; // x = x + 5
            x -= 1; // x = x - 1
            x *= 10; // x = x * 10
            x /= 2; // x = x / 2

            x++; // x = x + 1
            x--; // x = x - 1

            Console.WriteLine("Hello World!");
        }
    }
}
